package syntaxes;

public class ForLoopDemo {


	public static void main(String[] args) {

		// for loop beging with x = 2
		// and runs till x <= 4
		System.out.println("The syntax of for loop condition is (variable initiation, condition, increment/decrement operator)");
		for (int x =2; x <= 4 ; x++) {
			
			System.out.println(" Value of x is : "+ x);
		}

	}
}
